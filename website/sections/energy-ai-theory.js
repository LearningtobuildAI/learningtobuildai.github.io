/* energy-ai-theory.js — AI Energy Theory module.
   • "Generate" asks the local AI (/api/chat → Ollama) for a novel, grounded
     energy concept, then auto-SAVES it (versioned v1.0, v2.0…) server-side.
   • Saved theories are listed below the card; any can be PINNED to the top.
   100% local; degrades gracefully if the AI or store is unavailable. */
(function () {
  var btn = document.getElementById("aiTheoryGen");
  if (!btn) return;
  var titleEl = document.getElementById("aiTheoryTitle");
  var bodyEl = document.getElementById("aiTheoryBody");
  var noteEl = document.getElementById("aiTheoryNote");
  var savedEl = document.getElementById("aiTheorySaved");
  var savedHd = document.getElementById("aiTheorySavedHd");

  var PROMPT =
    "You are an energy-systems researcher AI. Propose ONE genuinely novel way to " +
    "generate or harvest energy that is NOT already common (not plain solar, wind, " +
    "hydro, fission, fusion, or a standard battery). It must be grounded in real " +
    "physics, biology or chemistry — imaginative but not magic. Reply as strict JSON " +
    'only: {"title":"3-6 word name","body":"120-160 words: what it is, then how it ' +
    'would work, and why an AI that can reason across many fields at once might be ' +
    'the one to make it practical."} No text outside the JSON.';

  function esc(s) {
    return String(s == null ? "" : s).replace(/[&<>"']/g, function (c) {
      return { "&": "&amp;", "<": "&lt;", ">": "&gt;", '"': "&quot;", "'": "&#39;" }[c];
    });
  }
  function extractJson(t) {
    var a = t.indexOf("{"), b = t.lastIndexOf("}");
    if (a < 0 || b <= a) return null;
    try { return JSON.parse(t.slice(a, b + 1)); } catch (e) { return null; }
  }

  function renderSaved(list) {
    if (!savedEl) return;
    if (!list || !list.length) { savedEl.innerHTML = ""; if (savedHd) savedHd.style.display = "none"; return; }
    // pinned first, then newest
    list.sort(function (a, b) { return (b.pinned ? 1 : 0) - (a.pinned ? 1 : 0); });
    if (savedHd) savedHd.style.display = "";
    savedEl.innerHTML = list
      .map(function (t) {
        return (
          '<div class="ai-theory-saved-card' + (t.pinned ? " pinned" : "") + '">' +
          '<div class="ai-theory-saved-top">' +
          '<span class="ai-theory-ver">' + esc(t.version || "v?") + "</span>" +
          '<span class="ai-theory-saved-title">' + esc(t.title) + "</span>" +
          '<button class="ai-theory-pin" data-id="' + esc(t.id) + '">' +
          (t.pinned ? "📌 Pinned" : "📍 Pin") + "</button>" +
          "</div>" +
          '<p class="ai-theory-saved-body">' + esc(t.body) + "</p>" +
          "</div>"
        );
      })
      .join("");
    savedEl.querySelectorAll(".ai-theory-pin").forEach(function (b) {
      b.addEventListener("click", function () {
        fetch("/api/energy-theories/pin", {
          method: "POST",
          headers: { "Content-Type": "application/json" },
          body: JSON.stringify({ id: b.getAttribute("data-id") }),
        }).then(function () { loadSaved(); }).catch(function () {});
      });
    });
  }
  function loadSaved() {
    fetch("/api/energy-theories")
      .then(function (r) { return r.ok ? r.json() : []; })
      .then(renderSaved)
      .catch(function () {});
  }

  function save(title, body) {
    return fetch("/api/energy-theories", {
      method: "POST",
      headers: { "Content-Type": "application/json" },
      body: JSON.stringify({ title: title, body: body }),
    })
      .then(function (r) { return r.ok ? r.json() : null; })
      .catch(function () { return null; });
  }

  btn.addEventListener("click", function () {
    btn.disabled = true;
    var old = btn.textContent;
    btn.textContent = "⏳ Thinking…";
    if (noteEl) noteEl.textContent = "The local AI is reasoning across fields…";
    fetch("/api/chat", {
      method: "POST",
      headers: { "Content-Type": "application/json" },
      body: JSON.stringify({ prompt: PROMPT }),
    })
      .then(function (r) { return r.ok ? r.json() : null; })
      .then(function (d) {
        var text = d && (d.text || d.answer || d.reply || d.response || d.message || d.content || "");
        var obj = text ? extractJson(text) : null;
        if (obj && obj.title && obj.body) {
          titleEl.textContent = obj.title;
          bodyEl.textContent = obj.body;
          return save(obj.title, obj.body).then(function (saved) {
            if (noteEl)
              noteEl.textContent = saved
                ? "Saved as " + saved.version + " · generated by the local AI, offline."
                : "Generated by the local AI (not saved — store unavailable).";
            loadSaved();
          });
        }
        if (text) {
          bodyEl.textContent = text.slice(0, 1200);
          if (noteEl) noteEl.textContent = "Local AI response (unstructured).";
        } else if (noteEl) {
          noteEl.textContent = "Local AI unavailable right now — showing the seed hypothesis.";
        }
      })
      .catch(function () {
        if (noteEl) noteEl.textContent =
          "Local AI unavailable (is the model running?) — showing the seed hypothesis.";
      })
      .then(function () {
        btn.disabled = false;
        btn.textContent = old;
      });
  });

  loadSaved();
})();
